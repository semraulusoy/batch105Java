package ders04_matematikselislemler_incrementdescrement;

public class C06_Pre_Post_Increment {
    public static void main(String[] args) {


        int a= 10;

        // a variablenin degerini yazdirip sonra a nin degerini 1 artirin


        System.out.println("a :" +a);
        a++;
        // a artik 11 ama kullanici konsolda gormez

        int b= 10;


        //b variablenin degerini 1 artirip sonra yazdirin

        b++;
        System.out.println("b : " + b);



        int c=10;
                //c variablenin degerini yazdirip, sonra c nin degerini bir artirin
        System.out.println( "c : "+ c++);

        int d=10;
                // d variablenin degerini bir artirip sonra yazdirin
        System.out.println("d:" + ++d);









    }
}
